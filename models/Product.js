class Product {
    constructor(id, ownerId, title, category, imageUrl, description, price) {
      this.id = id;
      this.ownerId = ownerId;
      this.title = title;
      this.category = category;
      this.imageUrl = imageUrl;
      this.description = description;
      this.price = price;
    }
  }
  
  export default Product;
  